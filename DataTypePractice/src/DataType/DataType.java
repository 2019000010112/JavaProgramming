package DataType;

public class DataType {
	public static void main(String[]args) {
		boolean b = true; //dynamic allocation
		char c;
		int i;
		float f;
		short s;
		double d;
		
		b=true;
		System.out.println("Your expected value : " +b);
		
		c='S';
		System.out.println("First Latter of your name is:" +c);
		
		i=2;
		System.out.println("Your position is:"+i);
		
		f=200.30f;
		System.out.println("The float value is:"+f);
		
		s=20;
		System.out.println("The expected value is:"+s);
		
		d=20222;
		System.out.println("The float value is:"+d);
	}

}
