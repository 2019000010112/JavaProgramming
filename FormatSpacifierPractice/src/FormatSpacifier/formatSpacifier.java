package FormatSpacifier;

public class formatSpacifier {
	public static void main(String[]args) {
		boolean b = true;
		String a = "Sajedul Islam";
		int i =5;
		double d = 44;
		float f = 40.2f;
		
		System.out.printf("The boolean of b %b \n", b);
		System.out.printf("The output of a is %s\n", a);
		System.out.printf("The output of i is %d\n", i);
		System.out.printf("The output of d is %f\n", d);
		System.out.printf("The output of f is %f\n", f);
	}
	

}
