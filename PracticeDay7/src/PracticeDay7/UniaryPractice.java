package PracticeDay7;

public class UniaryPractice {
	public static void main(String [] args) {
		int x=10;
		int result1 =+x;
		System.out.print("Your Unary Number is:"+result1);
		int result2 =-x;
		System.out.print("\nYour Unary Number is:"+result2);
		int result3 =++x;
		System.out.print("\nYour Unary Number is:"+result3);
		int result4 =--x;
		System.out.print("\nYour Unary Number is:"+result4);
	}

}
